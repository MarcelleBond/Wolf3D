#include "wolf3d.h"

void		color_w(t_rays m, t_color *w)
{
	if (m.dist < m.depth / 3.0)
	{
		w->c[0] = 0;
		w->c[1] = 0;
		w->c[2] = 240;
	}
	else if (m.dist < m.depth / 2.0)
	{
		w->c[0] = 0;
		w->c[1] = 0;
		w->c[2] = 220;
	}
	else if (m.dist < m.depth)
	{
		w->c[0] = 0;
		w->c[1] = 0;
		w->c[2] = 200;
	}
	else
	{
		w->c[0] = 70;
		w->c[1] = 73;
		w->c[2] = 79;
	}
}

void		color_f(float g, int y, t_color *f)
{
	g = 1.0 - (((float)y - HEIGHT / 2.0) / ((float)HEIGHT / 2.0));
	if (g < 0.5)
	{
		f->c[0] = 200;
		f->c[1] = 0;
		f->c[2] = 0;
	}
	else if (g < 0.9)
	{
		f->c[0] = 190;
		f->c[1] = 0;
		f->c[2] = 0;
	}
	else if (g < 0.75)
	{
		f->c[0] = 180;
		f->c[1] = 0;
		f->c[2] = 0;
	}
	else
	{
		f->c[0] = 170;
		f->c[1] = 0;
		f->c[2] = 0;
	}
}
